public class PrzypisanieWynikuLength {
  public static void main(String[] args) {
    String dzienTygodnia = "Poniedziałek";

    int liczbaZnakow = dzienTygodnia.length();

    System.out.println(
        "Dzień tygodnia \"" + dzienTygodnia +
        "\" składa się z " + liczbaZnakow + " znaków."
    );
  }
}
