public class TworzenieTablicNewZInicjalizatorem {
  public static void main(String[] args) {
    char[] znakiSlowaWitaj =
        new char[] { 'W', 'i', 't', 'a', 'j' };

    String[] papierKamienNozyce;
    papierKamienNozyce =
        new String[] { "papier", "kamień", "nożyce" };

    // wypisze pierwszy element, czyli literę W
    System.out.println(znakiSlowaWitaj[0]);

    // wypisze drugi element, czyli kamień
    System.out.println(papierKamienNozyce[1]);
  }
}
