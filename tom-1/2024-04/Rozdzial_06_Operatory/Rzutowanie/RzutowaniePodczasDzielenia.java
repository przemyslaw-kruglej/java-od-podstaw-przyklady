public class RzutowaniePodczasDzielenia {
  public static void main(String[] args) {
    System.out.println(
        "10 podzielone przez 4 to: " + (double) 10 / 4
    );

    int liczbaCalkowita = 77;
    int innaLiczbaCalkowita = 6;

    System.out.println(
        "77 dzielona przez 6 to " +
        (double) liczbaCalkowita / innaLiczbaCalkowita
    );
  }
}
