public class PomocniczeOperatoryPrzypisania {
  public static void main(String[] args) {
    int x = 5;
    x += 10;  // x = x + 10;
    x -= 3;   // x = x - 3;
    x *= 5;   // x = x * 5;
    x /= 12;  // x = x / 12;
    x %= 3;   // x = x % 3;
    x <<= 1;  // x = x << 1;
    x >>= 2;  // x = x >> 2;
    x >>>= 1; // x = x >>> 1;
    x &= 100; // x = x & 100;
    x |= 80;  // x = x | 80;
    x ^= 50;  // x = x ^ 50;
  }
}
