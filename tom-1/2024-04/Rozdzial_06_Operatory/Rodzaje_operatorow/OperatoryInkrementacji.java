public class OperatoryInkrementacji {
  public static void main(String[] args) {
    int x = 0;
    int y = 0;
    int a = 0;
    int b = 0;

    x++;
    ++y;

    System.out.println("x wynosi " + x);
    System.out.println("y wynosi " + y);

    System.out.println("a wynosi " + a++);
    System.out.println("b wynosi " + ++b);
  }
}
