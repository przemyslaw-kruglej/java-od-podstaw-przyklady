public class LiczbaLong {
  public static void main(String[] args) {
    long wielkaLiczba;

    // linia zakomentowana, ponieważ spowodowałaby
    // błąd kompilacji
    // wielkaLiczba = 10000000000;

    // ponieważ na końcu liczby dopisana jest litera L,
    // kompilator wie, że będzie to duża liczba
    wielkaLiczba = 10000000000L;
  }
}
