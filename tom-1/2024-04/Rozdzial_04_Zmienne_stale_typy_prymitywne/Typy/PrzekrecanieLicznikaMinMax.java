public class PrzekrecanieLicznikaMinMax {
  public static void main(String[] args) {
    int maksymalnaLiczbaPlus1 = 2147483647 + 1;
    int minimalnaLiczbaMinus1 = -2147483648 - 1;

    System.out.println(
        "Maksymalna plus 1 to: " + maksymalnaLiczbaPlus1
    );
    System.out.println(
        "Minimalna minus 1 to: " + minimalnaLiczbaMinus1
    );
  }
}
